/** id/order 없이 콘텐츠만 넣을 때 타입 단언용 (유니온 리터럴 초과속성 검사 회피) */
type ContentWithoutIdOrder = Omit<Content, 'id' | 'order'>;

/** 미리 생성된 문제가 있는 책 ID */
export const PRE_GENERATED_BOOK_IDS = ['book-mary', 'book-mongmong', 'book-stick-pebble', 'book-big-ear'] as const;

export function isPreGeneratedBook(bookId: string): boolean {
  return (PRE_GENERATED_BOOK_IDS as readonly string[]).includes(bookId);
}

function shuffle<T>(arr: T[]): T[] {
  const out = [...arr];
  for (let i = out.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [out[i], out[j]] = [out[j], out[i]];
  }
  return out;
}

function withIdsAndOrder(
  contents: Array<ContentWithoutIdOrder | (Record<string, unknown> & { type: ContentType })>,
  baseId: string
): Content[] {
  const list = contents as ContentWithoutIdOrder[];
  return list.map((c, i) => ({
    ...c,
    id: `${baseId}-${i}`,
    order: i + 1,
  })) as Content[];
}

