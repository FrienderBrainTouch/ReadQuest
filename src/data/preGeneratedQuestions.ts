/** explanation에 "정답인 이유:"와 "틀린 이유:"가 있으면 나누어 explanationCorrect, explanationWrong으로 설정 */
function ensureExplanationCorrectWrong(c: Content): Content {
  const exp = c.explanation || '';
  const correctIdx = exp.indexOf('정답인 이유:');
  const wrongIdx = exp.indexOf('틀린 이유:');
  if (correctIdx === -1 || wrongIdx === -1 || wrongIdx <= correctIdx) return c;
  const explanationCorrect = exp.slice(correctIdx, wrongIdx).trim();
  const explanationWrong = exp.slice(wrongIdx).trim();
  return { ...c, explanationCorrect, explanationWrong } as Content;
}

/**
 * 해당 책·활동에 미리 생성된 문제가 있으면 랜덤 순서로 섞어서 반환하고, 없으면 null.
 */
export function getPreGeneratedContents(bookId: string, contentType: ContentType): Content[] | null {
  const bookData = PREDEFINED[bookId];
  if (!bookData) return null;
  const list = bookData[contentType];
  if (!list || list.length === 0) return null;
  const shuffled = shuffle(list);
  const withOrder = withIdsAndOrder(
    shuffled.map(({ id: _id, order: _order, ...rest }) => rest) as Omit<Content, 'id' | 'order'>[],
    `pre-${bookId}-${contentType}`
  );
  return withOrder.map(ensureExplanationCorrectWrong);
}

/** 해당 책에 미리 생성된 콘텐츠가 있는 활동 타입 목록 */
export function getAvailableContentTypes(bookId: string): ContentType[] {
  const bookData = PREDEFINED[bookId];
  if (!bookData) return [];
  return (Object.keys(bookData) as ContentType[]).filter(
    (type) => bookData[type] && bookData[type].length > 0
  );
}
